$('.often_servers1').mouseover(function(){
    $('#tend').css({
        'top':'30px','left':'40%','width':'36px','height':'36px','transition':'all 0.01s'
    });
    $('#tend_h4').css({
        'top':'40px','transition':'all 0.01s'
    });
    $('#sigin').css({
        'top':'50px','transition':'all 0.01s'
    });
});
$('.often_servers1').mouseout(function(){
    $('#tend').css({
        'width':'72px','height':'72px','top':'50px','left':'30%'
    });
    $('#tend_h4').css({
        'top':'70px'
    });
    $('#sigin').css({
        'top':'100px'
    });
});
